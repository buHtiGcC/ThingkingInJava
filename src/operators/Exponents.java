package operators;

public class Exponents {
	public static void main(String[] args) {
		float minExpFloat = Float.MIN_VALUE;
		float maxExpFloat = Float.MAX_VALUE;
		double minExpDouble = Double.MIN_VALUE;
		double maxExpDouble = Double.MAX_VALUE;
		System.out.println("min float: " + minExpFloat);
		System.out.println("max float: " + maxExpFloat);
		System.out.println("min double: " + minExpDouble);
		System.out.println("max double: " + maxExpDouble);
	}
}
